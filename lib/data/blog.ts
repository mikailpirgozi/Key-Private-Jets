export function getBlogPostBySlug(slug: string) {
  return BLOG_POSTS.find((post) => post.slug === slug)
}

export function getBlogPostsByCategory(category: BlogPost['category']) {
  return BLOG_POSTS.filter((post) => post.category === category)
}

export function getRelatedBlogPosts(postId: string, limit: number = 3) {
  const post = BLOG_POSTS.find((p) => p.id === postId)
  if (!post) return []

  return BLOG_POSTS.filter(
    (p) => p.id !== postId && (p.category === post.category || p.keywords.some((kw) => post.keywords.includes(kw)))
  ).slice(0, limit)
}
